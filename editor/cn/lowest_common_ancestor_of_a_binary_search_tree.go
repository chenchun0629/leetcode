/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val   int
 *     Left  *TreeNode
 *     Right *TreeNode
 * }
 */

func lowestCommonAncestor(root, p, q *TreeNode) *TreeNode {
	var r = root

	for r != nil {
		if p.Val < r.Val && q.Val < r.Val {
			r = r.Left
		} else if p.Val > r.Val && q.Val > r.Val {
			r = r.Right
		} else {
			return r
		}
	}

	return r
}

