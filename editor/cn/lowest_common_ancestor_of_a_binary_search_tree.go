func lowestCommonAncestorGetPath(root, p *TreeNode) []*TreeNode {
	if root == nil {
		return nil
	}

	if root.Val == p.Val {
		return []*TreeNode{root}
	}

	var l = lowestCommonAncestorGetPath(root.Left, p)

	if l != nil {
		l = append([]*TreeNode{root}, l...)
		return l
	}

	l = lowestCommonAncestorGetPath(root.Right, p)
	if l != nil {
		l = append([]*TreeNode{root}, l...)
		return l
	}

	return l
}
