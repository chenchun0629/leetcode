func preorderTraversal_A(root *TreeNode) []int {
	if root == nil {
		return nil
	}

	var r = make([]int, 0)

	r = append(r, root.Val)

	if c := preorderTraversal(root.Left); c != nil {
		r = append(r, c...)
	}

	if c := preorderTraversal(root.Right); c != nil {
		r = append(r, c...)
	}

	return r
}
