func reverseBits_A(num uint32) uint32 {
	//fmt.Println(fmt.Sprintf("%b", num), "================a")
	var s = []rune(fmt.Sprintf("%032s", fmt.Sprintf("%b", num)))
	var b, e = 0, len(s) - 1
	for b < e {
		s[b], s[e] = s[e], s[b]
		b++
		e--
	}

	var i, _ = strconv.ParseInt(string(s), 2, 64)
	//fmt.Println(string(s), i, err, "================b")
	return uint32(i)
}
