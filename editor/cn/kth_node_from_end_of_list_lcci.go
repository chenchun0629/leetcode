/**
 * Definition for singly-linked list.
 * type ListNode struct {
 *     Val int
 *     Next *ListNode
 * }
 */
func kthToLast(head *ListNode, k int) int {
	var (
		bn = head
		en = head
	)

	for i := 0; bn != nil; bn = bn.Next {
		if i < k {
			i++
		} else {
			en = en.Next
		}
	}

	return en.Val
}

