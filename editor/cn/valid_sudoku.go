// ÁúÅÁ©∫Èó¥
func isValidSudoku_A(board [][]byte) bool {
	var (
		isValid = func(lb, le, wb, we int) bool {
			var (
				m = make(map[byte]struct{})
			)

			for i := lb; i < le; i++ {
				for j := wb; j < we; j++ {
					if board[i][j] == '.' {
						continue
					}

					if _, has := m[board[i][j]]; has {
						return false
					}
					m[board[i][j]] = struct{}{}
				}
			}
			return true
		}
	)

	for i := 0; i < 9; i++ {
		if !isValid(i, i+1, 0, 9) {
			return false
		}

		if !isValid(0, 9, i, i+1) {
			return false
		}
	}

	for i := 0; i < 3; i++ {
		for j := 0; j < 3; j++ {
			if !isValid(3*i, 3*(i+1), 3*j, 3*(j+1)) {
				return false
			}
		}
	}

	return true
}
